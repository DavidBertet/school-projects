GLuint		init_font(void)
{
  GLuint	i = 0;
  GLuint	fontOffset;

  glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
  fontOffset = glGenLists(255);
  for (; i < 255; i++)
    {
      glNewList(fontOffset + i, GL_COMPILE);
      glBitmap(11, 8, 0, 1, 12, 0, letters[i]);
      glEndList();
    }
  return (fontOffset);
}

void		print_my_string(char	*s,
				GLuint	offset)
{
  GLubyte	*mycast;
  void		*tmp;

  glPushAttrib(GL_LIST_BIT);
  glListBase(offset);
  tmp = s;
  mycast = tmp;
  glCallLists(strlen(s), GL_UNSIGNED_BYTE, mycast);
  glPopAttrib();
  return;
}
